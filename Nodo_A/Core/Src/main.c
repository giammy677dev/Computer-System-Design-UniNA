void delay (uint16_t time)
{
	__HAL_TIM_SET_COUNTER(&htim1, 0);
	while (__HAL_TIM_GET_COUNTER (&htim1) < time);
}

uint32_t IC_Val1 = 0;
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;  //verifichiamo se catturiamo il primo valore
uint8_t Distance  = 0;

#define TRIG_PIN GPIO_PIN_9
#define TRIG_PORT GPIOA
#define BUFFER_LEN 3

uint8_t RX_BUFFER[BUFFER_LEN] = {0, 0, 0};
uint8_t TX_BUFFER[BUFFER_LEN] = {0, 0, 0};


void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  //verifichiamo se l'interruzione viene da Channel 1
	{
		if (Is_First_Captured==0)
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); //catturiamo il valore...
			Is_First_Captured = 1; //... e settiamo la variabile ad 1
			//Mettiamo sul fronte di discesa
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured==1)   //se abbiamo giÃ  il primo valore
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  //catturiamo il secondo valore
			__HAL_TIM_SET_COUNTER(htim, 0);  //resettiamo il conteggio del timer

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = Difference * 0.3438/2;
			Is_First_Captured = 0;

			//Rimettiamo sul fronte di salita
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC1);
		}
	}
}

void HCSR04_Read (void)
{
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);  //mettiamo TRIG alto
	delay(10);  //aspettiamo 10 microsecondi
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  //mettiamo TRIG basso

	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC1);
}

